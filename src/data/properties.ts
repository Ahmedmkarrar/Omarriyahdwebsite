export const formatPrice = (price: number): string => {
  return new Intl.NumberFormat("en-US", {
    style: "currency",
    currency: "USD",
    minimumFractionDigits: 0,
    maximumFractionDigits: 0,
  }).format(price);
};

export const formatNumber = (num: number): string => {
  return new Intl.NumberFormat("en-US").format(num);
};

export const getStatusBadgeClass = (status: Property["status"]): string => {
  switch (status) {
    case "active":
      return "badge-status-active";
    case "pending":
      return "badge-status-pending";
    case "sold":
      return "badge-status-sold";
    case "off-market":
      return "badge-luxury";
    default:
      return "badge-status-active";
  }
};

export const getStatusLabel = (status: Property["status"]): string => {
  switch (status) {
    case "active":
      return "Active";
    case "pending":
      return "Pending";
    case "sold":
      return "Sold";
    case "off-market":
      return "Off-Market";
    default:
      return status;
  }
};
